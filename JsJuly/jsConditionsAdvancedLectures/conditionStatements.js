function demo() {

    let color = `green`;

    switch (color) {
        case `blue`: console.log(`the sky is blue`);
            break;
        case `green`: console.log(`the grass is green`);
            break;
        case `yellow`: console.log(`the sun is yellow`);
            break;
        default: console.log(`different color`)
    }

}
demo();

